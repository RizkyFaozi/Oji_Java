    private String validasiForm() {
        if( usernamee.getText().isEmpty() ) {
            return "Username wajib diisi";
        }
        
        if( paswordd.getText().isEmpty() ) {
            return "Password wajib diisi";
        }
        
        return "";

    }//GEN-LAST:event_jButton1ActionPerformed

