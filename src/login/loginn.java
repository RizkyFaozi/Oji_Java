    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String validasi = this.validasiForm();
        if(validasi.isEmpty()) {
            String username = usernamee.getText();
            String password = paswordd.getText();
            int sabagai = sebagai.getSelectedIndex();
            ResultModel akun;
            
            switch(sabagai) {
                case 1:
                case 2:
                    akun = new Petugas().dataAkun(username);
                    break;
                    
                default:
                    akun = new Masyarakat().dataAkun(username);
            }
            
            if(akun == null) {
                HelperClass.showAlert(this, 0, "Username anda tidak terdaftar");
            } else if(!akun.dByColumn("password").equals(password)) {
                HelperClass.showAlert(this, 0, "Password yang anda masukkan salah");
            
            } else {
                HelperClass.showAlert(this, 1, "Login berhasil!");
                setVisible(false);
                
                switch(sabagai) {
                    case 1:
                        new petugas.petugass(akun).setVisible(true);
                        break;
                    case 2:
                        new admin.data_laporan(akun).setVisible(true);
                        break;
                        
                    default:
                        new masyarakat.masyarakatt(akun).setVisible(true);
                }
            }
            
        } else {
            HelperClass.showAlert(this, 0, validasi);
        }
    }                                         

