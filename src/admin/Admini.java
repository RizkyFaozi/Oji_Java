    private void verfy1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verfy1ActionPerformed
        // TODO add your handling code here
        int rowSelected = jTable2.getSelectedRow();
        if(rowSelected >= 0) {
            ResultModel laporanSelected = this.dataLaporan[rowSelected];
            if(laporanSelected.dByColumn("status").equals("0")) {
                String id = this.dataLaporan[rowSelected].dByColumn("id_pengaduan");
                boolean verif = this.laporanModel.verifikasi(id);
                
                if(verif) {
                    HelperClass.showAlert(this, 1, "Laporan berhasil diverifikasi");
                    setVisible(false);
                    new Admini(this.user).setVisible(true);
                } else {
                    HelperClass.showAlert(this, 0, "Laporan gagal di verifikasi, ERROR");
                }
                
            } else {
                HelperClass.showAlert(this, 0, "Laporan yang anda pilih sudah di verifikasi");
            }
            
        } else {
            HelperClass.showAlert(this, 0, "Harap pilih data laporan terlebih dahulu!");
        }
    }//GEN-LAST:event_verfy1ActionPerformed

