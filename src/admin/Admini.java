    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        int rowSelected = jTable2.getSelectedRow();
        if(rowSelected >= 0) {
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapus data petugas?");
            if(konfirmasi == 0) {
                String id = this.dataLaporan[rowSelected].dByColumn("id_pengaduan");
                boolean delete = this.laporanModel.delete(id);
                
                if(delete) {
                    HelperClass.showAlert(this, 1, "Berhasil menghapus data laporan");
                    HelperClass.deleteRowTable(jTable2, rowSelected);
                    this.dataLaporan = this.laporanModel.allLaporanPetugas();
                } else {
                    HelperClass.showAlert(this, 0, "Gagal menghapus data laporan, ERROR.");
                }
            }
            
        } else {
            HelperClass.showAlert(this, 0, "Harap pilih data laporan terlebih dahulu!");
        }
    }//GEN-LAST:event_HapusActionPerformed

    
    public void tampilkanLaporan() {
        DefaultTableModel tModel = (DefaultTableModel) jTable2.getModel();
        for(int i=0; i < this.dataLaporan.length; i++) {
            ResultModel cDLaporan = this.dataLaporan[i];
            tModel.addRow(new String[] {
                cDLaporan.dByColumn("id_pengaduan"),
                cDLaporan.dByColumn("nama_masyarakat"),
                cDLaporan.dByColumn("tgl_pengaduan"),
                cDLaporan.dByColumn("status"),
            });
        }
        
        HelperClass.centerTabelCell(jTable2);
    }

